                    <h2>The King's Men</h2>
                    <p>
                        Six shillings and eight pence is what Shakespeare’s company of actors, the Lord Chamberlain’s Men, were charged for the
                        first stage of a bureaucratic process to gain a licence granting them the patronage of the new king, James I; henceforth,
                        the company would be known as the King’s Men. It was expensive – at the time, actors in London were normally paid less
                        than one shilling a day – but it was to prove a wise investment.
                    </p>
                    <p>
                        This information has recently come to light in a new examination of a document in The National Archives by Dr Hannah
                        Crummé.
                    </p>
